    private void btnSacuvajVlasnikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajVlasnikaActionPerformed
        try {
            validacija();
            String ime = txtIme.getText();
            String prezime = txtPrezime.getText();
            String brojTelefona = txtBrojTelefona.getText();
            
            Vlasnik v = new Vlasnik();
            v.setIme(ime);
            v.setPrezime(prezime);
            v.setBrojTelefona(brojTelefona);
            
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.DODAJ_VLASNIKA);
            kz.setParametar(v);
            
            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            Vlasnik vlasnik = (Vlasnik) so.getOdgovor();
            if(vlasnik != null){
                JOptionPane.showMessageDialog(this, "Vlasnik je uspesno ubacen u sistem.");
                srediFormu();
            }else{
                JOptionPane.showMessageDialog(this, "Vlasnik nije ubacen! Pokusajte ponovo.");
                srediFormu();
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSacuvajVlasnikaActionPerformed


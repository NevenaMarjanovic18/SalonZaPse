    private void validacija() throws Exception {
        String greska = "";
        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        String brojTelefona = txtBrojTelefona.getText();
        boolean samoSlovaIme = samoSlova(ime);
        boolean samoSlovaPrezime = samoSlova(prezime);
        boolean samoCifreIPlus = samoCifreIPlus(brojTelefona);

        if (txtIme.getText().isEmpty()) {
            greska += "Morate uneti ime!\n";
        }
        if (!samoSlovaIme) {
            greska += "Ime mora da sadrzi samo slova!\n";
        }
        if (txtPrezime.getText().isEmpty()) {
            greska += "Morate uneti prezime!\n";
        }
        if (!samoSlovaPrezime) {
            greska += "Prezime mora sadrzati samo slova!\n";
        }
        if (txtBrojTelefona.getText().isEmpty()) {
            greska += "Morate uneti broj telefona!\n";
        }
        if (!brojTelefona.startsWith("+3816")) {
            greska += "Broj telefona mora poceti sa '+3816'!\n";
        }
        if(brojTelefona.length() != 13){
            greska += "Broj telefona mora da sadrzi 13 cifara! Pokusajte ponovo. \n";
        }
        if (!greska.isEmpty()) {
            throw new Exception(greska);
        }
    }

    private boolean samoSlova(String string) {
        for (Character slovo : string.toCharArray()) {
            if (!Character.isLetter(slovo)) {
                return false;
            }
        }
        return true;
    }

    private boolean samoCifreIPlus(String string) {
        for (Character slovo : string.toCharArray()) {
            if (slovo == '+') {
                continue;
            }
            if (!Character.isDigit(slovo)) {
                return false;
            }
        }
        return true;
    }

    private void srediFormu() {
        txtIme.setText("");
        txtPrezime.setText("");
        txtBrojTelefona.setText("");
    }

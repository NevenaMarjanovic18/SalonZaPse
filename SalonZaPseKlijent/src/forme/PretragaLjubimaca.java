    private void btnIzmeniLjubimcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniLjubimcaActionPerformed
        int red = tblLjubimci.getSelectedRow();
        if(red == -1){
            JOptionPane.showMessageDialog(this, "Izaberite ljubimca koga zelite da izmenite!");
        }else{
            ModelTabeleLjubimci mt = (ModelTabeleLjubimci) tblLjubimci.getModel();
            Ljubimac lj = (Ljubimac) mt.getLista().get(red);
            IzmenaLjubimca iz = new IzmenaLjubimca(this, true, lj);
            iz.setVisible(true);
            //this.setVisible(false);
        }
    }//GEN-LAST:event_btnIzmeniLjubimcaActionPerformed

    private void btnObrisiLjubimcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiLjubimcaActionPerformed
        int red = tblLjubimci.getSelectedRow();
        if(red == -1){
            JOptionPane.showMessageDialog(this, "Izaberite ljubimca koga zelite da obrisete!");
        }else{
            ModelTabeleLjubimci mt = (ModelTabeleLjubimci) tblLjubimci.getModel();
            Ljubimac lj = (Ljubimac) mt.getLista().get(red);
            int potvrda = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite obrisati izabranog ljubimca?");
            if(potvrda == 0){
                KlijentskiZahtev kz = new KlijentskiZahtev();
                kz.setOperacija(Operacije.OBRISI_LJUBIMCA);
                kz.setParametar(lj);
                Komunikacija.getInstance().posaljiZahtev(kz);
                ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
                Ljubimac obrisaniLjubimac = (Ljubimac) so.getOdgovor();
                if(obrisaniLjubimac != null){
                    JOptionPane.showMessageDialog(this, "Ljubimac je uspesno izbrisan iz sistema.");
                }else{
                    JOptionPane.showMessageDialog(this, "Greska prilikom brisanja ljubimca. Pokusajte ponovo.");
                }
            }
        }
    }//GEN-LAST:event_btnObrisiLjubimcaActionPerformed


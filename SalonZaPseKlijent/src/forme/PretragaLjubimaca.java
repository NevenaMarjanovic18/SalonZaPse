    private void btnPretraziLjubimceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziLjubimceActionPerformed
        Vlasnik vl = (Vlasnik) cmbVlasnik.getSelectedItem();
        Ljubimac lj = new Ljubimac();
        lj.setVlasnik(vl);
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.NADJI_LJUBIMCE);
        kz.setParametar(lj);
        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        List<OpstiDomenskiObjekat> listaLjubimaca = (List<OpstiDomenskiObjekat>) so.getOdgovor();
        ModelTabeleLjubimci mt = (ModelTabeleLjubimci) tblLjubimci.getModel();
        mt.popuniTabelu(listaLjubimaca);
        
    }//GEN-LAST:event_btnPretraziLjubimceActionPerformed


    private void popuniTabelu() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_LJUBIMCE);
        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        List<OpstiDomenskiObjekat> listaLjubimaca = (List<OpstiDomenskiObjekat>) so.getOdgovor();
        ModelTabeleLjubimci mt = (ModelTabeleLjubimci) tblLjubimci.getModel();
        mt.dodajLjubimceUListu(listaLjubimaca);
    }

    private void popuniComboVlasnika() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_VLASNIKE);
        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        List<OpstiDomenskiObjekat> listaVlasnika = (List<OpstiDomenskiObjekat>) so.getOdgovor();
        cmbVlasnik.removeAllItems();
        for (OpstiDomenskiObjekat vlasnik : listaVlasnika) {
            cmbVlasnik.addItem(vlasnik);
        }
    }
